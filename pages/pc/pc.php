<h2>How to create a desktop shortcut</h2>

<img src="http://puu.sh/gM15F/9b87e4b2c2.jpg" alt="Shortcuts">

<p>Instructions for Chrome:</p>

<ol>
	<li>Open <a href="https://animereleasenotifier.com/web/" target="_blank">https://animereleasenotifier.com/web/</a> in a new tab</li>
	<li>While you're on the new tab click on the Settings button > More tools > Create application shortcuts...</li>
	<li>Activate both options if you wish to pin it to your task bar and on your desktop</li>
	<li>Confirm by clicking on <strong>Create</strong></li>
</ol>